static const char *auto_locate_sidecar(uint32_t fw_id) {
    // Glob `tools/symbols/*.symbols.txt` and match by `# fw-id: <id>` header, so every
    // model's store auto-registers (no hardcoded list — a new bring-up's sidecar is
    // picked up the moment gen_symbols_txt.py writes it). Returns a static buf; NULL if
    // none. SYMDIR overrides the directory.
    static char found[1024];
    char target[16];
    snprintf(target, sizeof target, "%08x", fw_id);
    const char *dir = getenv("SYMDIR");
    if (!dir || !*dir) dir = "tools/symbols";
    DIR *d = opendir(dir);
    if (!d) return NULL;
    struct dirent *de;
    while ((de = readdir(d))) {
        size_t n = strlen(de->d_name);
        if (n < 12 || strcmp(de->d_name + n - 12, ".symbols.txt") != 0) continue;
        char path[1024];
        snprintf(path, sizeof path, "%s/%s", dir, de->d_name);
        FILE *f = fopen(path, "r");
        if (!f) continue;
        char line[256];
        while (fgets(line, sizeof line, f)) {
            if (strstr(line, "# fw-id:") && strstr(line, target)) {
                fclose(f);
                closedir(d);
                snprintf(found, sizeof found, "%s", path);
                return found;
            }
            if (line[0] != '#' && line[0] != '\n' && line[0] != '[') break;  // past header
        }
        fclose(f);
    }
    closedir(d);
    return NULL;
}

int main(int argc, char **argv) {
    const char *flash_path = NULL;
    const char *addr_str = NULL;
    const char *count_str = NULL;
    int thumb = 1;
    const char *symbols_path = NULL;
    const char *ram_path = NULL;
    const char *strings_path = NULL;
    const char *struct_str = NULL;
    uint32_t struct_window = 0x80;
    int annotate = 1;

    for (int i = 1; i < argc; i++) {
        if (!strcmp(argv[i], "--symbols") && i + 1 < argc) symbols_path = argv[++i];
        else if (!strcmp(argv[i], "--ram") && i + 1 < argc) ram_path = argv[++i];
        else if (!strcmp(argv[i], "--strings") && i + 1 < argc) strings_path = argv[++i];
        else if (!strcmp(argv[i], "--struct") && i + 1 < argc) struct_str = argv[++i];
        else if (!strcmp(argv[i], "--structsize") && i + 1 < argc) struct_window = (uint32_t)strtoul(argv[++i], NULL, 0);
        else if (!strcmp(argv[i], "--no-annot"))           annotate = 0;
        else if (!strcmp(argv[i], "arm"))                  thumb = 0;
        else if (!flash_path)                              flash_path = argv[i];
        else if (!addr_str)                                addr_str = argv[i];
        else if (!count_str)                               count_str = argv[i];
    }
    if (!flash_path || (!addr_str && !struct_str)) {
        fprintf(stderr,
                "usage: %s <flash.fls> <addr> [count] [arm] [--symbols PATH] [--ram DUMP] [--no-annot]\n"
                "       %s <flash.fls> --struct 0xBASE [--structsize N] [--ram DUMP]\n",
                argv[0], argv[0]);
        return 1;
    }

    FILE *f = fopen(flash_path, "rb");
    if (!f) { perror("open"); return 1; }
    fseek(f, 0, SEEK_END);
    long n = ftell(f);
    fseek(f, 0, SEEK_SET);
    uint8_t *d = malloc((size_t)n);
    if (fread(d, 1, (size_t)n, f) != (size_t)n) return 1;
    fclose(f);
    g_flash = d; g_flash_n = (size_t)n;

    if (ram_path) {
        FILE *rf = fopen(ram_path, "rb");
        if (!rf) { perror("open --ram"); return 1; }
        fseek(rf, 0, SEEK_END);
        long rn = ftell(rf);
        fseek(rf, 0, SEEK_SET);
        uint8_t *rd = malloc((size_t)rn);
        if (!rd || fread(rd, 1, (size_t)rn, rf) != (size_t)rn) return 1;
        fclose(rf);
        g_ram = rd; g_ram_n = (size_t)rn;
        fprintf(stderr, "[disfw] RAM snapshot: %s (%ld bytes) — values/tables resolve live\n",
                ram_path, rn);
    }

    uint32_t fw_id = fw_build_id(d, (size_t)n);
    uint32_t addr = addr_str ? (uint32_t)strtoul(addr_str, NULL, 0) : 0;
    int count = count_str ? atoi(count_str) : 32;

    if (annotate) {
        if (!symbols_path) symbols_path = auto_locate_sidecar(fw_id);
        if (symbols_path && load_symbols(symbols_path)) {
            fprintf(stderr, "[disfw] symbols: %s  (%d addrs, %d msgs, %d reasons)\n",
                    symbols_path, g_addrs_n, g_msgs_n, g_rsns_n);
            // Also auto-load a sibling scratchpad if present — session RE log.
            // Same .txt format as the [addresses] section of symbols.txt (the
            // [addresses] section header is missing, but load_symbols accepts
            // bare `<hex> <NAME>` lines after a `[addresses]` marker; we keep
            // scratch.txt prefixed with that header — see tools/symbols/scratch.py).
            char scratch[1024];
            const char *dot = strstr(symbols_path, ".symbols.txt");
            if (dot) {
                size_t base_len = (size_t)(dot - symbols_path);
                snprintf(scratch, sizeof scratch, "%.*s.scratch.txt",
                         (int)base_len, symbols_path);
                int before_addrs = g_addrs_n, before_msgs = g_msgs_n, before_rsns = g_rsns_n;
                if (load_symbols(scratch)) {
                    int added = (g_addrs_n - before_addrs) + (g_msgs_n - before_msgs)
                              + (g_rsns_n - before_rsns);
                    if (added > 0) {
                        fprintf(stderr, "[disfw] scratchpad: %s  (+%d entries)\n",
                                scratch, added);
                        // load_symbols qsorts post-insert, so the addr table
                        // is re-sorted with scratch entries — binary search
                        // still finds them.
                    }
                }
            }
        } else if (annotate) {
            fprintf(stderr, "[disfw] no symbols sidecar for fw-id %08x — running un-annotated\n", fw_id);
        }
    }

    DCT3Core *core = dct3_core_create();
    dct3_write_bytes(core, BASE, d, (size_t)n);
    reg_reset_all();

    // --struct 0xBASE: whole-image field setter/reader xref, then exit.
    if (struct_str) {
        uint32_t sbase = (uint32_t)strtoul(struct_str, NULL, 0);
        struct_xref(core, sbase, struct_window ? struct_window : 0x80);
        dct3_core_destroy(core);
        free(d);
        if (g_ram) free((void *)g_ram);
        return 0;
    }

    // Build the BL-caller + table-entry reverse map (once). Annotation-only;
    // also useful even if symbols missed, but we only run when annotation is on.
    if (annotate) {
        xref_scan(d, (size_t)n);
        fprintf(stderr, "[disfw] xref: %d refs scanned\n", g_xref_n);
    }
    if (strings_path) {
        load_strings(strings_path);
        g_getstr_a  = addr_by_name("GET_STRING");
        g_wgetstr_a = addr_by_name("W_GET_STRING");
        if (!g_getstr_a) g_getstr_a = addr_by_name("PPM_STRING_LOOKUP");
    }

    char buf[256];
    char line[320];
    char annot[256];
    char peep[128];
    uint32_t a = addr;
    for (int i = 0; i < count; ++i) {
        uint32_t o = a - BASE;
        struct ARMInstructionInfo info;
        int line_bytes = 4;     // bytes consumed by this instruction
        if (thumb) {
            uint16_t op = (uint16_t)((d[o] << 8) | d[o + 1]);
            ARMDecodeThumb(op, &info);
            if ((op & 0xF800) == 0xF000) {
                struct ARMInstructionInfo info2, combined;
                uint16_t op2 = (uint16_t)((d[o + 2] << 8) | d[o + 3]);
                ARMDecodeThumb(op2, &info2);
                if (ARMDecodeThumbCombine(&info, &info2, &combined)) {
                    ARMDisassemble(&combined, &core->cpu, NULL, a + 4, buf, sizeof buf);
                    snprintf(line, sizeof line, "%08X: %04X %04X  %s", a, op, op2, buf);
                    line_bytes = 4;
                } else {
                    ARMDisassemble(&info, &core->cpu, NULL, a + 4, buf, sizeof buf);
                    snprintf(line, sizeof line, "%08X: %04X       %s", a, op, buf);
                    line_bytes = 2;
                }
            } else {
                ARMDisassemble(&info, &core->cpu, NULL, a + 4, buf, sizeof buf);
                snprintf(line, sizeof line, "%08X: %04X       %s", a, op, buf);
                line_bytes = 2;
            }
        } else {
            uint32_t op = ((uint32_t)d[o] << 24) | (d[o + 1] << 16) | (d[o + 2] << 8) | d[o + 3];
            ARMDecodeARM(op, &info);
            ARMDisassemble(&info, &core->cpu, NULL, a + 8, buf, sizeof buf);
            snprintf(line, sizeof line, "%08X: %08X  %s", a, op, buf);
            line_bytes = 4;
        }

        if (annotate) {
            // Prologue banner: if this address is a known symbol AND the bytes
            // look like a Thumb push prologue (0xB5xx / 0xB4xx) OR it's the
            // first insn we're emitting, print a banner line above with the
            // function name + caller/table summary.
            const char *fn_name = sym_lookup(g_addrs, g_addrs_n, a);
            uint8_t b0 = d[a - BASE];
            int is_prologue_byte = (b0 == 0xB5 || b0 == 0xB4);
            if (fn_name && (is_prologue_byte || i == 0)) {
                int n_bl = 0, n_arm_bl = 0, n_tab = 0;
                uint32_t bls[4], arm_bls[4], tabs[4];
                int n_bls = 0, n_arm_bls = 0, n_tabs = 0;
                xref_summary(a, &n_bl, &n_arm_bl, &n_tab,
                             bls,    &n_bls,    4,
                             arm_bls,&n_arm_bls,4,
                             tabs,   &n_tabs,   4);
                printf("\n; ==== %s @ 0x%08X  (%d bl + %d arm-bl callers, %d table refs) ====\n",
                       fn_name, a, n_bl, n_arm_bl, n_tab);
                if (n_bls > 0) {
                    printf(";   bl from: ");
                    for (int k = 0; k < n_bls; k++) {
                        uint32_t lr = bls[k];
                        const char *near = NULL;
                        int idx = sym_nearest(g_addrs, g_addrs_n, lr);
                        if (idx >= 0 && (lr - g_addrs[idx].key) < 0x1000)
                            near = g_addrs[idx].name;
                        printf("0x%06X%s%s%s ", lr,
                               near ? " (" : "", near ? near : "", near ? ")" : "");
                    }
                    if (n_bl > n_bls) printf("...+%d more", n_bl - n_bls);
                    printf("\n");
                }
                if (n_arm_bls > 0) {
                    printf(";   ARM bl from: ");
                    for (int k = 0; k < n_arm_bls; k++) {
                        uint32_t lr = arm_bls[k];
                        const char *near = NULL;
                        int idx = sym_nearest(g_addrs, g_addrs_n, lr);
                        if (idx >= 0 && (lr - g_addrs[idx].key) < 0x1000)
                            near = g_addrs[idx].name;
                        printf("0x%06X%s%s%s ", lr,
                               near ? " (" : "", near ? near : "", near ? ")" : "");
                    }
                    if (n_arm_bl > n_arm_bls) printf("...+%d more", n_arm_bl - n_arm_bls);
                    printf("\n");
                }
                if (n_tabs > 0) {
                    printf(";   table at: ");
                    for (int k = 0; k < n_tabs; k++) printf("0x%06X ", tabs[k]);
                    if (n_tab > n_tabs) printf("...+%d more", n_tab - n_tabs);
                    printf("\n");
                }
            }

            annot_for_line(line, annot, sizeof annot, a, !thumb);
            peephole_annot(line, peep, sizeof peep);
            if (peep[0]) {
                size_t alen = strlen(annot);
                if (alen) snprintf(annot + alen, sizeof annot - alen, "  -- %s", peep);
                else      snprintf(annot, sizeof annot, "%s", peep);
            }
            // Effective-address (tracked-pointer) annotation — must run BEFORE
            // update_peephole consumes this line, so it sees the register state as of
            // the access.
            char mem[160];
            mem_annot(line, mem, sizeof mem);
            if (mem[0]) {
                size_t alen = strlen(annot);
                snprintf(annot + alen, sizeof annot - alen, "%s%s",
                         alen ? "  " : "", mem);
            }
            char flow[96];
            flow_annot(line, flow, sizeof flow);
            if (flow[0]) {
                size_t alen = strlen(annot);
                snprintf(annot + alen, sizeof annot - alen, "%s%s",
                         alen ? "  " : "", flow);
            }
            char strn[128];
            str_annot(line, strn, sizeof strn);
            if (strn[0]) {
                size_t alen = strlen(annot);
                snprintf(annot + alen, sizeof annot - alen, "%s%s",
                         alen ? "  " : "", strn);
            }
            emit(line, annot);
            update_peephole(line, a);
        } else {
            printf("%s\n", line);
        }
        a += (uint32_t)line_bytes;
    }

    free(d);
    return 0;
}
